package Allprograming;

public class RelationOperator {

	public static void main(String[] args) {
		int a=28,b=39;
		//Equals to using ==
		System.out.println(a==b);
		//Not equals to using !=
		System.out.println(a!=b);
		//Less than using <
		System.out.println(a<b);
		//Less than equate using <=
		System.out.println(a<=b);
		//Grater than using >
		System.out.println(a>b);
		//Grater than equate using >=
		System.out.println(a>=b);
		
	}
}
