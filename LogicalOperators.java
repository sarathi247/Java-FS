package Allprograming;

public class LogicalOperators {
	public static void main(String[] args) {
		int a=20,b=14;
		//And operator using && .
		System.out.println((a>b)&&(a<b));
		
		//OR operator using ||
		System.out.println((a>b)||(a<b));
		
		//NOT operator using !
		System.out.println(!(a>b));
	}

}
